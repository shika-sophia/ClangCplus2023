/*
◆練習問題5.4 p134
 1. 1以上 30以下の数をコンソールに表示。ただし 6の倍数は表示しないプログラム。
*/

#include <stdio.h>

int main(void)
{
    for (int i = 1; i <= 30; i++) {
        if (i % 6 == 0) {
            continue;
        }

        printf("%i, ", i);
    }//for

    puts("\n");
    return 0;
}//main

